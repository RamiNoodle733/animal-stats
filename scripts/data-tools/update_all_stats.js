async function updateAllStats() {
    try {
        console.log('Connecting to MongoDB...');
        await mongoose.connect(MONGODB_URI);
        console.log('Connected!\n');

        const db = mongoose.connection.db;
        const collection = db.collection('animals');

        let agilityUpdated = 0;
        let attackUpdated = 0;
        let defenseUpdated = 0;

        // Update Agility
        console.log('=== UPDATING AGILITY STATS ===');
        for (const [animalName, value] of Object.entries(agilityStats)) {
            const result = await collection.updateOne(
                { name: animalName },
                { $set: { agility: value } }
            );
            if (result.matchedCount > 0) agilityUpdated++;
        }
        console.log(`✓ Updated ${agilityUpdated} agility stats`);

        // Update Attack
        console.log('\n=== UPDATING ATTACK STATS ===');
        for (const [animalName, value] of Object.entries(attackStats)) {
            const result = await collection.updateOne(
                { name: animalName },
                { $set: { attack: value } }
            );
            if (result.matchedCount > 0) attackUpdated++;
        }
        console.log(`✓ Updated ${attackUpdated} attack stats`);

        // Update Defense
        console.log('\n=== UPDATING DEFENSE STATS ===');
        for (const [animalName, value] of Object.entries(defenseStats)) {
            const result = await collection.updateOne(
                { name: animalName },
                { $set: { defense: value } }
            );
            if (result.matchedCount > 0) defenseUpdated++;
        }
        console.log(`✓ Updated ${defenseUpdated} defense stats`);

        // Verification
        console.log('\n========================================');
        console.log('VERIFICATION - Top 5 for each stat:');
        
        console.log('\nTop 5 Attack:');
        const topAttack = await collection.find({}).sort({ attack: -1 }).limit(5).toArray();
        topAttack.forEach((a, i) => console.log(`  ${i+1}. ${a.name}: ${a.attack}`));

        console.log('\nTop 5 Defense:');
        const topDefense = await collection.find({}).sort({ defense: -1 }).limit(5).toArray();
        topDefense.forEach((a, i) => console.log(`  ${i+1}. ${a.name}: ${a.defense}`));

        console.log('\nTop 5 Agility:');
        const topAgility = await collection.find({}).sort({ agility: -1 }).limit(5).toArray();
        topAgility.forEach((a, i) => console.log(`  ${i+1}. ${a.name}: ${a.agility}`));

    } catch (error) {
        console.error('Error:', error);
    } finally {
        await mongoose.disconnect();
        console.log('\nDisconnected from MongoDB');
    }
}

